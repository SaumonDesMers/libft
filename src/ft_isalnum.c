#include "libft.h"

int	ft_isalnum(int c)
{
	int	ret;

	ret = 0;
	if (c >= '0' && c <= '9')
		ret = 1;
	if (c >= 'a' && c <= 'z')
		ret = 1;
	if (c >= 'A' && c <= 'Z')
		ret = 1;
	return (ret);
}
